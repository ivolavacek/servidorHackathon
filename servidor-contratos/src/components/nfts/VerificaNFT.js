const { ethers } = require('ethers');


const contractAddressERC721 = '0x275Fa8A81baEf004f1072A60b57e33e2B09530e5';

const provider = new ethers.JsonRpcProvider('https://sepolia.optimism.io');

const contractERC721 = new ethers.Contract(contractAddressERC721, contractABI, provider);

async function verificarNFT() {
  try {
    const provider = new ethers.BrowserProvider(window.ethereum);
    const signer = await provider.getSigner();
    const connectedContract = contractERC721.connect(signer);
    const resultado = await connectedContract.ownsToken(signer.address);
    console.log('Resultado da função:', resultado);
    return resultado
  } catch (error) {
    console.error('Erro ao chamar a função:', error);
  }
}

